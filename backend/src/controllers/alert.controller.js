const alertService = require('../services/alert.service');
const { sendSuccess, sendCreated } = require('../utils/responseFormatter');
const { pool } = require('../config/database');

module.exports = {
  async list(req, res) {
    const result = await alertService.listAlerts({ ...req.query, baseId: req.user.base_id });
    sendSuccess(res, result);
  },

  async acknowledge(req, res) {
    const alert = await alertService.acknowledgeAlert(req.params.id, req.user);
    sendSuccess(res, alert, 'Alert acknowledged');
  },

  async resolve(req, res) {
    const alert = await alertService.resolveAlert(req.params.id, req.user, req.body.resolution_notes);
    sendSuccess(res, alert, 'Alert resolved');
  },

  async dismiss(req, res) {
    const alert = await alertService.dismissAlert(req.params.id, req.user);
    sendSuccess(res, alert, 'Alert dismissed');
  },

  async acknowledgeAll(req, res) {
    const baseId = req.user.base_id;
    const { rowCount } = await pool.query(
      `UPDATE alerts SET status='ACKNOWLEDGED', acknowledged_by_id=$1, acknowledged_at=NOW()
       WHERE status='OPEN' AND base_id=$2`,
      [req.user.id, baseId]
    );
    sendSuccess(res, { updated: rowCount }, `${rowCount} alerts acknowledged`);
  },

  async create(req, res) {
    const alert = await alertService.createAlert({
      ...req.body,
      baseId:        req.body.base_id || req.user.base_id,
      autoGenerated: false,
    });
    sendCreated(res, alert, 'Alert created');
  },
};
